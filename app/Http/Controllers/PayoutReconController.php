    public function reconPayout(Request $request){
        $validator = Validator::make($request->all(), [
            "bankdata"  => "required|string",
        ]);
        if($validator->fails()) {
            $error = $validator->errors()->first();
            return response()->json(['status' => false, 'message' => $error])->setStatusCode(400);
        }

        try {

            Log::channel('payout_recon_file')->debug('transaction_v0',
                [
                    'all_data' => $request->all(),
                    'all_header' => $request->headers->all(),
                ]
            );

            if ($request->hasHeader('token')) {
                $headerToken = $request->header('token');
                if (strcmp($headerToken, '6BGm1VH2sfg3LZqvl8vaaeQacjkJ8VtB9s9q05jLcAQ1LZC0gV') === 0) {
                    Log::channel('payout_recon_file')->debug('transaction_v1',
                        [
                            'all_data' => $request->all(),
                            'all_header' => $request->headers->all(),
                        ]
                    );
                    $bankdata = json_decode($request->bankdata, true);
                    Log::channel('payout_recon_file')->debug('transaction_v1',
                        [
                            '$bankdata' => $bankdata
                        ]
                    );
                    foreach ($bankdata as $dt) {
                        $bankUtrNumber = "";
                        if (isset($dt['utr']) && !empty($dt['utr'])) {
                            $bankUtrNumber = $dt['utr'];
                        }

                        Log::channel('payout_recon_file')->debug('transaction_v1',
                            [
                                '$bankUtrNumber' => $bankUtrNumber
                            ]
                        );

                        if (!empty($bankUtrNumber)) {
                            $payoutDetails = (new Payout())->checkUtrExists($bankUtrNumber);
                            if (isset($payoutDetails) && !empty($payoutDetails)) {
                                $payoutId = $payoutDetails->payout_id;
                                $merchantId = $payoutDetails->merchant_id;
                                $manualPayBatchId = $payoutDetails->manual_pay_batch_id;

                                $payoutRec = new stdClass();
                                $payoutRec->payoutId = $payoutId;
                                $payoutRec->merchantId = $merchantId;
                                $payoutRec->manualPayBatchId = $manualPayBatchId;

                                Log::channel('payout_recon_file')->debug('transaction_v1',
                                    [
                                        '$payoutId' => $payoutId
                                    ]
                                );

                                $added = (new PayoutManualReconciliation())->addReconPayout($payoutId, $merchantId, $manualPayBatchId);
                                if ($added) {
                                    print_r($payoutRec);
                                    echo "\n " . $bankUtrNumber . " - " . $payoutId . " Record Added Success \n";
                                } else {
                                    echo "\n " . $bankUtrNumber . " - " . $payoutId . " Skipped \n";
                                }
                            } else {
                                echo "\n " . $bankUtrNumber . " - " . " Skipped \n";
                            }
                        }
                    }
                }
                return "ok";
            }
        }catch (\Exception $ex){
            Log::error('Error while executing SQL Query', [
                'class' => __CLASS__,
                'function' => __METHOD__,
                'file' => $ex->getFile(),
                'line_no' => $ex->getLine(),
                'error_message' => $ex->getMessage(),
            ]);
        }

        return response()->json(['status' => false, 'message' => "Mandatory fields are missing in Headers"])->setStatusCode(400);
    }

